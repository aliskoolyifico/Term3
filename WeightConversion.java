
public class WeightConversion 
{
	public static void main(String[] args) 
	{
        System.out.println("Hello World!"); // Display the string.
    }

	public double KilosToPounds (double kilos)
	{
		
		return (kilos / 0.454);
	}
	
    public double PoundsToKilos (double pounds)
    {
		
		return (pounds * 0.454);
	}
}
